  <h1>Quem Somos</h1>
  <p>A Escola Técnica Estadual (ETEC) é uma instituição de ensino técnico e tecnológico presente no estado de São Paulo, Brasil. Desde sua fundação em 2006, temos o compromisso de oferecer educação de qualidade e formação profissional em diversas áreas do conhecimento, preparando nossos estudantes para ingressarem no mercado de trabalho ou prosseguirem seus estudos em níveis superiores.</p>
  <p>Na ETEC, acreditamos na formação integral do aluno, combinando o desenvolvimento de habilidades técnicas com uma sólida base acadêmica. Nossa equipe de profissionais altamente qualificados está empenhada em proporcionar um ambiente de aprendizagem estimulante e desafiador, onde os alunos podem crescer e se desenvolver tanto pessoal quanto profissionalmente.</p>

  <h2>Nossa Missão</h2>
  <p>Nossa missão é proporcionar educação de qualidade, preparando os alunos para os desafios do mercado de trabalho e para contribuir de forma positiva para o progresso da sociedade.</p>

